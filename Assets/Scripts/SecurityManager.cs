using UnityEngine;

public class SecurityManager : MonoBehaviour
{
    //синглтон
    public static SecurityManager instance { get; private set; }

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(this.gameObject);
            return;
        }
        Destroy(this.gameObject);
    }

}
